export function mapFilterCounts(pins: AdminMapPin[]) {
    const count = (fn: (p: AdminMapPin) => boolean) => pins.filter(fn).length;

    return {
        sev_all: pins.length,
        sev_critical: count((p) => p.severity === 'critical'),
        sev_high: count((p) => p.severity === 'high'),
        sev_medium: count((p) => p.severity === 'medium'),
        sev_low: count((p) => p.severity === 'low'),
        status_all: pins.length,
        status_active: count((p) => p.status === 'active'),
        status_pending: count((p) => p.status === 'pending'),
        status_resolved: count((p) => p.status === 'resolved'),
        type_all: pins.length,
        type_fire: count((p) => p.type_icon === 'fire'),
        type_flood: count((p) => p.type_icon === 'flood'),
        type_drainage: count((p) => p.type_icon === 'drainage'),
        type_waste: count((p) => p.type_icon === 'waste'),
        type_noise: count((p) => p.type_icon === 'noise'),
        type_light: count((p) => p.type_icon === 'light'),
    };
}

export function pinsFromReports(reports: AdminReport[]): AdminMapPin[] {
    return reports
        .filter((r) => !r.location.toLowerCase().includes('withheld') && !r.location.startsWith('Duplicate'))
        .map((r, i) => ({
            id: `mp-${r.concern_id}`,
            lat: 14.5995 + (i % 4) * 0.002 - 0.003,
            lng: 120.9842 + (Math.floor(i / 4) % 3) * 0.003 - 0.004,
            title: r.incident_type,
            severity: severityFromScore(r.ai_severity),
            report_id: r.id,
            concern_id: r.concern_id,
            location_label: r.location,
            type_icon: r.type_icon,
            incident_type: r.incident_type,
            status:
                r.queue_status === 'active' || r.status === 'ongoing'
                    ? 'active'
                    : r.status === 'done'
                      ? 'resolved'
                      : 'pending',
            has_mission: false,
            ai_severity: r.ai_severity,
        }));
}

