export function residentCounts(residents: AdminResident[]) {
    return {
        all: residents.length,
        approved: residents.filter((r) => r.verification_status === 'approved').length,
        pending: residents.filter((r) => r.verification_status === 'pending').length,
        in_progress: residents.filter((r) => r.verification_status === 'in_progress').length,
        rejected: residents.filter((r) => r.verification_status === 'rejected').length,
    };
}

export function findResident(id: string): AdminResidentDetail | undefined {
    const base = demoResidents.find((r) => r.id === id || r.account_id === id);
    if (!base) return undefined;
    const extra = residentDetails[base.id] ?? defaultDetail;
    return { ...base, ...extra };
}
